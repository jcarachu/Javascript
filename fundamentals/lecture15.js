const id = '300';

// Equal to
if (id == 100)
{
  console.log('correct');

} else {
  console.log('Incorrect');
}

// Not equal to
if(id != 101) {
  console.log('correct');
} else {
  console.log('Incorrect');
}

// Else to value and type
if(id === 100)
{
  console.log('correct');
} else {
  console.log('incorrect');
}

// Else to value and type
if(id !== 100)
{
  console.log('correct');
} else {
  console.log('incorrect');
}

if(typeof id !== 'undefined'){
  console.log(`The ID is ${id}`);
} else {
  console.log('No id');
}

// Greater or less than
if (id > 200){
  console.log('Correct');
} else {
  console.log('Inncorrect');
}

